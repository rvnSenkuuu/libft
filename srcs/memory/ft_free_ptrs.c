#include "libft.h"

void	ft_free_ptrs(void **ptr)
{
	size_t	i;

	i = 0;
	while (ptr && ptr[i])
	{
		(free(ptr[i]), ptr[i] = NULL);
		i++;
	}
	(free(ptr), ptr = NULL);
}
